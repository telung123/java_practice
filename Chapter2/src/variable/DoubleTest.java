package variable;

public class DoubleTest {

	public static void main(String[] args) {
		
		double dNum = 3.14;
		// java 에서 실수형의 기본 데이터형은 double.
		
		float fNum = 3.14F;
		// 4byte 인 float 는 double value 를 담을 수 없음. 뒤에 'F' 식별자를 붙여줘야 함
		
		System.out.println(dNum);
		System.out.println(fNum);

	}

}
